function decodeWord(word: string): string {
    return word.split(" ").map((x: string) => MORSE_CODE[x]).join('');
}
export function decodeMorse(morseCode: string): string {
    return morseCode.trim().split("   ").map(word => decodeWord(word)).join(" ");
}


